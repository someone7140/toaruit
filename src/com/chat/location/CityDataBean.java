
package com.chat.location;

public class CityDataBean {

    private String id;
    private String name;
  
    public CityDataBean() {}

    public String getId() {
    	return this.id;
    }

    public void setId(String id) {
    	this.id = id;
    }
    
    public String getName() {
    	return this.name;
    }

    public void setName(String name) {
    	this.name = name;
    }
    
}
