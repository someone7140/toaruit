
package com.giin.search;




public class SangiinBean {

	private String senkyoKubun;
    private String todouhuken;
    private String senkyoku;
    private String giinName;
    private String seitou;
    private String ninkiManryoubi;

    public SangiinBean() {}

    public String getSenkyoKubun() {
    	return this.senkyoKubun;
    }

    public void setSenkyoKubun(String senkyoKubun) {
    	this.senkyoKubun = senkyoKubun;
    }

    public String getTodouhuken() {
    	return this.todouhuken;
    }

    public void setTodouhuken(String todouhuken) {
    	this.todouhuken = todouhuken;
    }
    
    public String getSenkyoku() {
    	return this.senkyoku;
    }

    public void setSenkyoku(String senkyoku) {
    	this.senkyoku = senkyoku;
    }

    public String getGiinName() {
    	return this.giinName;
    }

    public void setGiinName(String giinName) {
    	this.giinName = giinName;
    }

    public String getSeitou() {
    	return this.seitou;
    }

    public void setSeitou(String seitou) {
    	this.seitou = seitou;
    }

    public String getNinkiManryoubi() {
    	return this.ninkiManryoubi;
    }

    public void setNinkiManryoubi(String ninkiManryoubi) {
    	this.ninkiManryoubi = ninkiManryoubi;
    }
    

}
